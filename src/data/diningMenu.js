export const parsePrice = (price) => Number(String(price).replace(/[^0-9]/g, ''));

export const createFoodCheckoutItem = ({ title, description, image, price, type, category }) => ({
  id: `${type}-${title}`.toLowerCase().replace(/[^a-z0-9]+/g, '-'),
  title,
  description,
  image,
  amount: parsePrice(price),
  type,
  category,
});

export const allFoodCheckoutItems = [
  ...defaultFeaturedPlates.map((plate) =>
    createFoodCheckoutItem({
      title: plate.title,
      description: plate.description,
      image: plate.image,
      price: plate.price,
      type: 'Featured Dish',
      category: plate.tag,
    })
  ),
  ...defaultDiningCategories.flatMap((category) =>
    category.items.map((item) =>
      createFoodCheckoutItem({
        title: item.name,
        description: `${category.title} selection`,
        image: item.image,
        price: item.price,
        type: 'Food Order',
        category: category.title,
      })
    )
  ),
];
